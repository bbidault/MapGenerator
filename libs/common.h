#include "ctime"

/// square root of 2
#define SQRT_2 sqrt( 2 )

/** test if a value x is between two margins
 *
 * @param number to test
 * @param low margin
 * @param high margin
 */
bool between( double aX,
              double aA,
              double aB );

/** return absolute value |a|
 *
 * @param a
 */
int abs( int aA );

/** return x^y
 *
 * @param x
 * @param y
 */
int pow( int aX,
         int aY );
