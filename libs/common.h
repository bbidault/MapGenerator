/** test if a value x is between two margins
 * 
 * @param number to test
 * @param low margin
 * @param high margin
 */
bool between( double x, double a, double b );

/** return absolute value |a|
 * 
 * @param a
 */
int abs( int a );

/** return x^y
 * 
 * @param x
 * @param y
 */
int pow( int x, int y );
